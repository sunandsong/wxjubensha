  // 名字替换器：把剧本里的角色名（如「小美」）整体替换成扮演该角色的玩家昵称。
  // 没人扮演的「公开嫌疑人」(NPC) 保留原角色名。供 game / result 页统一使用。
  makeNamer(SCRIPT, players) {
    const chars = SCRIPT.characters || [];
    const list = players || [];
    const map = {}; // 角色名 -> 显示名（昵称或原名）
    chars.forEach((c) => {
      const owner = list.find((p) => p.charId === c.id);
      map[c.name] = owner && owner.nick ? owner.nick : c.name;
    });
    // 仅对「真的有昵称、和原名不同」的角色名建正则，长名优先，避免误替换
    const names = Object.keys(map)
      .filter((n) => map[n] !== n)
      .sort((a, b) => b.length - a.length);
    const re = names.length ? new RegExp(names.join("|"), "g") : null;
    return {
      // 取某角色的显示名
      name(charId) {
        const c = chars.find((x) => x.id === charId);
        return c ? map[c.name] : "";
      },
      // 替换一段文本里出现的所有角色名
      apply(str) {
        return re && typeof str === "string"
          ? str.replace(re, (m) => map[m] || m)
          : str;
      },
    };
  },
